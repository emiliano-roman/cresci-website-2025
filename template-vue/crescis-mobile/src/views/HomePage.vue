<script>
import 'swiper/swiper-bundle.css'; // Importar estilo de Swiper
import $ from 'jquery'; // Importar jQuery
import Swiper from 'swiper'; // Importar Swiper
import { initializeLenis, pageTransition, menuSite, navbarFix, updateHeaderTheme, animationsEfx, textScrolling } from '../assets/js/scriptsApp.js'; // Importar funciones de otros scripts
import gsap from 'gsap'; // Importar GSAP
import { ScrollTrigger } from 'gsap/ScrollTrigger'; // Importar ScrollTrigger
import { initTextAnimations } from '../assets/js/textAnimations.js'; // Importar animaciones de texto

gsap.registerPlugin(ScrollTrigger); // Plugin ScrollTrigger 

export default {
    name: 'HomePage',
    data() {
        return {
            lenis: null, // Definimos lenis aquí
        };
    },
    mounted() {
        this.lenis = initializeLenis(); // Inicializamos lenis al montar el componente
        this.initializeComponents(); // Llamamos a la función para inicializar otros componentes
    },
    methods: {
        initializeComponents() {
            // Inicializa otros componentes
            pageTransition();
            menuSite();
            navbarFix();
            updateHeaderTheme();
            animationsEfx();
            textScrolling();
            this.$nextTick(() => {
                initTextAnimations();
            });
            // Opciones de Swiper
            const swiperOptions = {
                slidesPerView: 'auto',
                centeredSlides: false,
                loop: false,
                slidesPerGroup: 1,
                freeMode: true,
                spaceBetween: 0,
                speed: 1000,
                grabCursor: true,
                scrollbar: {
                    el: ".swiper-scrollbar",
                    hide: false,
                },
                breakpoints: {
                    640: {
                        slidesPerView: 'auto',
                        spaceBetween: 0,
                        scrollbar: true,
                    },
                    768: {
                        slidesPerView: 'auto',
                        spaceBetween: 0,
                        scrollbar: true,
                    },
                    1024: {
                        slidesPerView: 'auto',
                        spaceBetween: 0,
                        slidesPerGroup: 1,
                        scrollbar: false,
                    },
                    1940: {
                        slidesPerView: 4,
                        spaceBetween: 0,
                        slidesPerGroup: 1,
                        scrollbar: false,
                    },
                },
            };

            // eslint-disable-next-line no-unused-vars
            const swiper = new Swiper('.swiper-container', swiperOptions);

            // BG CHANGE COLOR
            this.setupScrollTriggers();

            // VIDEO REEL HOME
            this.setupVideoReel();

            // INICIA LA ROTACIÓN DE IMÁGENES
            this.setupImagesTrail();
        },
        setupScrollTriggers() {
            gsap.registerPlugin(ScrollTrigger);

            // Seleccionar los logos
            const logoDark = document.querySelector('.logo-dark-in');
            const logoWhite = document.querySelector('.logo-white-in');

            // Verificar que los logos existan en el DOM antes de continuar
            if (!logoDark || !logoWhite) {
                console.error('Los logos no se encontraron en el DOM');
                return; // Si no están presentes, detenemos la ejecución
            }

            // Inicialmente asegurarse de que el logo oscuro esté visible y el logo blanco oculto
            logoDark.classList.add('visible');
            logoDark.classList.remove('hidden');
            logoWhite.classList.add('hidden');
            logoWhite.classList.remove('visible');

            const darkSections = document.querySelectorAll('.dark-section');

            darkSections.forEach(section => {
                ScrollTrigger.create({
                    trigger: section,
                    start: '-5% center',
                    end: 'bottom center',
                    onEnter: () => {
                        section.classList.add('dark-active');
                        
                        // Cambiar visibilidad de los logos al entrar en la sección
                        console.log('Se activa dark-active, cambiando logos');
                        logoDark.classList.add('hidden');
                        logoDark.classList.remove('visible');
                        logoWhite.classList.add('visible');
                        logoWhite.classList.remove('hidden');
                    },
                    onLeaveBack: () => {
                        section.classList.remove('dark-active');
                        
                        // Volver a cambiar la visibilidad de los logos al salir de la sección
                        console.log('Se desactiva dark-active, cambiando logos');
                        logoDark.classList.add('visible');
                        logoDark.classList.remove('hidden');
                        logoWhite.classList.add('hidden');
                        logoWhite.classList.remove('visible');
                    }
                });
            });
        },

        setupVideoReel() { 
              // Seleccionar el segundo video dentro de la sección #closeReel
              const secondVideo = document.querySelector("#closeReel video"); 

              if (!secondVideo) {
                  console.error("No se encontró el segundo video en #closeReel");
                  return;
              }

              // Asegúrate de que el video esté en pausa al cargar la vista HOME
              secondVideo.pause();

              $("#closeReel").hide(); // Oculta el botón de cierre al inicio
              $("#openReel").show();  // Muestra el botón de inicio

              const toggleScroll = (shouldPrevent) => {
                  document.body.classList.toggle("no-scroll", shouldPrevent);
                  window[shouldPrevent ? 'addEventListener' : 'removeEventListener'](
                      'wheel',
                      (e) => e.preventDefault(),
                      { passive: false }
                  );
              };

              // Evento para cuando se hace clic en #openReel
              $("#openReel").click(() => {
                  this.lenis?.stop();
                  toggleScroll(true);
                  $("#overlay").show();

                  // Reproducir el segundo video y reiniciar el tiempo a 0
                  secondVideo.currentTime = 0;
                  secondVideo.play().then(() => {
                      console.log("Segundo video en reproducción");
                  }).catch((error) => {
                      console.error("Error al reproducir el segundo video:", error);
                  });

                  // Animación de transición
                  gsap.to("#openReel", {
                      duration: 0.3,
                      opacity: 0,
                      onComplete: () => {
                          $("#openReel").hide();
                          $("#closeReel").show();
                          gsap.fromTo("#closeReel", { scale: 0.5, opacity: 0 }, { duration: 0.5, scale: 1, opacity: 1 });
                      }
                  });
              });

              // Evento para cuando se hace clic en #closeReel
              $("#closeReel").click(() => {
                  this.lenis?.start();
                  toggleScroll(false);

                  // Pausar el segundo video
                  secondVideo.pause();
                  console.log("Segundo video pausado");

                  // Animación de transición
                  gsap.to("#closeReel", {
                      duration: 0.3,
                      opacity: 0,
                      onComplete: () => {
                          $("#closeReel").hide();
                          $("#overlay").hide();
                          $("#openReel").show();
                          gsap.to("#openReel", { duration: 0.3, opacity: 1 });
                      }
                  });
              });
        },

        setupImagesTrail() {
            const images = document.querySelectorAll('.content__img');
            let currentIndex = 0; // Índice de la imagen actual

            // Mostrar la primera imagen
            images[currentIndex].classList.add('active');

            setInterval(() => {
                // Ocultar la imagen actual
                images[currentIndex].classList.remove('active');
                // Incrementar el índice de forma aleatoria
                currentIndex = Math.floor(Math.random() * images.length); // Selecciona un nuevo índice aleatorio
                // Mostrar la nueva imagen
                images[currentIndex].classList.add('active');
            }, 300); // Cambia cada 2 segundos (puedes ajustar el tiempo)
        },
    },
